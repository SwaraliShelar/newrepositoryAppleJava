package AssignmentOnSwitchCase;

public class KIsMerisCampus {
	
	
	    public static void main(String args[]) {
	        int k = 65;
	        switch (k) {
	            default :
	                System.out.print("Website");
//because k=65 
//then it will  print values of 65 and after 65
//i.e case k and case j
	            case 65 :
	                System.out.print("Merit");
	            case 'k' :
	                System.out.print("Campus");
	            case 'j' :
	                System.out.print("Java");
	                break;
	        
	    }
	}


}
